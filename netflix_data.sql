CREATE TABLE Netflix_data (
    latitude FLOAT,
    longitude FLOAT,
    country VARCHAR(255),
    type VARCHAR(255),
    title VARCHAR(255),
    director VARCHAR(255),
    date_added Varchar,
    release_year INT,
    rating VARCHAR(255),
    duration VARCHAR(255),
    listed_in VARCHAR(255),
    country_count INT
);

select * from Netflix_data;